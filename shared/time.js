/** Compact mm:ss for popup timer */
export function formatCountdownClock(ms) {
  if (ms <= 0) return '0:00';
  const totalSec = Math.ceil(ms / 1000);
  const m = Math.floor(totalSec / 60);
  const s = totalSec % 60;
  return `${m}:${s.toString().padStart(2, '0')}`;
}

/** Longer copy for intervention page */
export function formatSessionRemainingPhrase(ms) {
  if (ms <= 0) return 'Session ending…';
  const totalSec = Math.ceil(ms / 1000);
  const m = Math.floor(totalSec / 60);
  const s = totalSec % 60;
  return `${m}m ${s.toString().padStart(2, '0')}s left`;
}
